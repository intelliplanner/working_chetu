package com.seniorline.beans;

public class AccountDto {

	private String name;

	private String language;

	private String consumerRole;

	private String enterpriseRole;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getConsumerRole() {
		return consumerRole;
	}

	public void setConsumerRole(String consumerRole) {
		this.consumerRole = consumerRole;
	}

	public String getEnterpriseRole() {
		return enterpriseRole;
	}

	public void setEnterpriseRole(String enterpriseRole) {
		this.enterpriseRole = enterpriseRole;
	}

}
